-- ============================================================================
-- ENERGY CONSUMPTION (~56,000 readings: 8K sites x 7 months)
-- ============================================================================

USE SCHEMA ENERGY;

INSERT INTO CONSUMPTION_READINGS (
    READING_ID, SITE_ID, METER_ID, READING_DATE, YEAR_MONTH,
    CONSUMPTION_KWH, PEAK_CONSUMPTION_KWH, OFF_PEAK_CONSUMPTION_KWH,
    ENERGY_COST_EUR, COST_PER_KWH_EUR,
    IS_PASSTHROUGH, PASSTHROUGH_AMOUNT_EUR,
    HEATING_DEGREE_DAYS, COOLING_DEGREE_DAYS,
    IS_ESTIMATED, DATA_QUALITY_SCORE, SOURCE_SYSTEM, SOURCE_RECORD_ID
)
SELECT 
    'CONS-' || LPAD(ROW_NUMBER() OVER (), 8, '0') AS READING_ID,
    s.SITE_ID,
    'MTR-' || s.SITE_ID AS METER_ID,
    cal.DATE_KEY AS READING_DATE,
    DATE_TRUNC('MONTH', cal.DATE_KEY) AS YEAR_MONTH,
    -- Seasonal variation: higher in winter (Nov-Dec) and summer cooling
    CASE 
        WHEN MONTH(cal.DATE_KEY) IN (11, 12) THEN UNIFORM(2000, 4000, RANDOM()) * 1.3  -- Winter +30%
        WHEN MONTH(cal.DATE_KEY) IN (7, 8) THEN UNIFORM(2000, 4000, RANDOM()) * 1.15   -- Summer +15%
        ELSE UNIFORM(2000, 4000, RANDOM())
    END AS CONSUMPTION_KWH,
    UNIFORM(800, 1600, RANDOM()) AS PEAK_CONSUMPTION_KWH,
    UNIFORM(1200, 2400, RANDOM()) AS OFF_PEAK_CONSUMPTION_KWH,
    UNIFORM(300, 800, RANDOM()) AS ENERGY_COST_EUR,
    UNIFORM(0.12, 0.25, RANDOM()) AS COST_PER_KWH_EUR,
    TRUE AS IS_PASSTHROUGH,  -- Energy is re-invoiced to customers
    UNIFORM(250, 700, RANDOM()) AS PASSTHROUGH_AMOUNT_EUR,
    CASE WHEN MONTH(cal.DATE_KEY) IN (11, 12, 1, 2) THEN UNIFORM(100, 300, RANDOM()) ELSE UNIFORM(0, 50, RANDOM()) END AS HEATING_DEGREE_DAYS,
    CASE WHEN MONTH(cal.DATE_KEY) IN (6, 7, 8) THEN UNIFORM(50, 150, RANDOM()) ELSE UNIFORM(0, 20, RANDOM()) END AS COOLING_DEGREE_DAYS,
    CASE WHEN UNIFORM(0, 100, RANDOM()) < 5 THEN TRUE ELSE FALSE END AS IS_ESTIMATED,
    UNIFORM(85, 100, RANDOM()) AS DATA_QUALITY_SCORE,
    'TDF_ENERGY_MGT' AS SOURCE_SYSTEM,
    'EN-' || LPAD(ROW_NUMBER() OVER (), 8, '0') AS SOURCE_RECORD_ID
FROM (
    SELECT DISTINCT DATE_TRUNC('MONTH', DATE_KEY) AS DATE_KEY 
    FROM TDF_DATA_PLATFORM.CORE.CALENDAR 
    WHERE DATE_KEY BETWEEN '2025-06-01' AND '2025-12-31'
) cal
CROSS JOIN (SELECT SITE_ID FROM TDF_DATA_PLATFORM.INFRASTRUCTURE.SITES WHERE STATUS = 'ACTIVE' LIMIT 8000) s;

-- ============================================================================
-- CARBON_EMISSIONS
-- ============================================================================

INSERT INTO CARBON_EMISSIONS (
    EMISSION_ID, YEAR_MONTH, FISCAL_YEAR,
    EMISSION_SCOPE, EMISSION_CATEGORY,
    SITE_ID, REGION_ID, BU_ID,
    EMISSIONS_KG_CO2E, ACTIVITY_QUANTITY, ACTIVITY_UNIT, EMISSION_FACTOR, EMISSION_FACTOR_SOURCE,
    REVENUE_EUR, CARBON_INTENSITY_KG_EUR,
    TARGET_EMISSIONS_KG, VARIANCE_TO_TARGET_KG,
    CALCULATION_METHOD, DATA_QUALITY_SCORE, IS_VERIFIED,
    SOURCE_SYSTEM, SOURCE_RECORD_ID
)
SELECT 
    'EMI-' || LPAD(ROW_NUMBER() OVER (), 7, '0') AS EMISSION_ID,
    cal.DATE_KEY AS YEAR_MONTH,
    2025 AS FISCAL_YEAR,
    CASE MOD(ROW_NUMBER() OVER (), 3)
        WHEN 0 THEN 'SCOPE_1'
        WHEN 1 THEN 'SCOPE_2'
        ELSE 'SCOPE_3'
    END AS EMISSION_SCOPE,
    CASE MOD(ROW_NUMBER() OVER (), 5)
        WHEN 0 THEN 'ELECTRICITY'
        WHEN 1 THEN 'ELECTRICITY'
        WHEN 2 THEN 'FUEL'
        WHEN 3 THEN 'VEHICLES'
        ELSE 'SUPPLY_CHAIN'
    END AS EMISSION_CATEGORY,
    NULL AS SITE_ID,
    r.REGION_ID,
    'BU-CORP' AS BU_ID,
    UNIFORM(50000, 500000, RANDOM()) AS EMISSIONS_KG_CO2E,
    UNIFORM(100000, 1000000, RANDOM()) AS ACTIVITY_QUANTITY,
    'kWh' AS ACTIVITY_UNIT,
    0.0571 AS EMISSION_FACTOR,  -- France electricity factor kg CO2/kWh
    'ADEME 2024' AS EMISSION_FACTOR_SOURCE,
    799100000/12/13 AS REVENUE_EUR,  -- Monthly revenue / 13 regions
    UNIFORM(0.05, 0.15, RANDOM()) AS CARBON_INTENSITY_KG_EUR,
    UNIFORM(45000, 450000, RANDOM()) AS TARGET_EMISSIONS_KG,
    UNIFORM(-50000, 50000, RANDOM()) AS VARIANCE_TO_TARGET_KG,
    'GHG Protocol' AS CALCULATION_METHOD,
    UNIFORM(80, 100, RANDOM()) AS DATA_QUALITY_SCORE,
    CASE WHEN UNIFORM(0, 100, RANDOM()) < 80 THEN TRUE ELSE FALSE END AS IS_VERIFIED,
    'TDF_ESG_PLATFORM' AS SOURCE_SYSTEM,
    'CO2-' || LPAD(ROW_NUMBER() OVER (), 7, '0') AS SOURCE_RECORD_ID
FROM (
    SELECT DISTINCT DATE_TRUNC('MONTH', DATE_KEY) AS DATE_KEY 
    FROM TDF_DATA_PLATFORM.CORE.CALENDAR 
    WHERE DATE_KEY BETWEEN '2025-06-01' AND '2025-12-31'
) cal
CROSS JOIN TDF_DATA_PLATFORM.CORE.REGIONS r;

