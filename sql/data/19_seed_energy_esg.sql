    SELECT DISTINCT DATE_TRUNC('MONTH', DATE_KEY) AS DATE_KEY 
    FROM TDF_DATA_PLATFORM.CORE.CALENDAR 
    WHERE DATE_KEY BETWEEN '2025-06-01' AND '2025-12-31'
